CREATE SEQUENCE IF NOT EXISTS  electronicdocument.EDSDOCUMENTTYPE START 1 INCREMENT 1 MINVALUE 1;

CREATE TABLE electronicdocument.EDTDOCUMENTTYPE
(
  ID NUMERIC(10) PRIMARY KEY DEFAULT nextval('electronicdocument.EDSDOCUMENTTYPE'),
  DOCUMENTCODE VARCHAR(2) UNIQUE NOT NULL ,
  DOCUMENTNAME VARCHAR(100) NOT NULL UNIQUE,
  DOCUMENTDETAILS VARCHAR(250) NOT NULL,
  STATUS_ID NUMERIC(10),
  STATUS_CODE VARCHAR(5),
  CONSTRAINT FK_DOCUMENTTYPE_STATUS FOREIGN KEY(STATUS_ID) REFERENCES electronicdocument.EDTSTATUS(ID) ON DELETE NO ACTION ON UPDATE CASCADE,
  CONSTRAINT FK_DOCUMENTTYPE_STATUSCODE FOREIGN KEY(STATUS_CODE) REFERENCES electronicdocument.EDTSTATUS(CODE) ON DELETE NO ACTION ON UPDATE CASCADE
);

